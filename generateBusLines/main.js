// Create a lookup map for efficient access to stops by name (case-insensitive)
const stopLookup = new Map()
stopsData.forEach((stop) => {
  if (stop && stop.name) {
    // Ensure stop and name exist
    stopLookup.set(stop.name.toLowerCase(), stop)
  } else {
    console.warn('Skipping invalid entry in stopsData:', stop)
  }
})

// Function to get ordered stops for a given list of names
function getOrderedStops(stopNames, directionName) {
  const orderedStops = []
  let stopsFoundCount = 0
  stopNames.forEach((name, index) => {
    // Normalize name for lookup (lowercase)
    const stopObject = stopLookup.get(name.toLowerCase())
    if (stopObject) {
      // Add only essential info + coords for the route file
      orderedStops.push({
        id: stopObject.id,
        name: stopObject.name, // Keep original name from JSON for consistency
        lat: stopObject.lat,
        lon: stopObject.lon,
      })
      stopsFoundCount++
    } else {
      console.warn(`WARNING (${directionName}): Stop "${name}" (index ${index}) from timetable not found in stopsData JSON.`)
    }
  })
  console.log(
    `Processed ${directionName}: Found ${stopsFoundCount} out of ${stopNames.length} stops from timetable in JSON data.`
  )
  return orderedStops
}

